#!/usr/bin/env node
/**
 * Scans apps/web/app/** for icon names used in `KunIcon` / `Icon` components and
 * generates `apps/web/icon/icon.ts` with two exports:
 *
 *   - ICON_NAMES: every `<collection>:<icon>` literal found
 *   - ICON_COLLECTIONS: the unique set of `<collection>` prefixes
 *
 * The output is consumed by `nuxt.config.ts`:
 *
 *   import { ICON_NAMES, ICON_COLLECTIONS } from './icon/icon'
 *   icon: {
 *     mode: 'svg',
 *     serverBundle: { collections: ICON_COLLECTIONS },
 *     clientBundle: { icons: ICON_NAMES, scan: false }
 *   }
 *
 * Both static (`name="lucide:x"`) and ternary (`:name="cond ? 'lucide:a' : 'lucide:b'"`)
 * usages are picked up. Dynamic names that come from variables (e.g.
 * `:name="item.icon"`) cannot be extracted statically — add those to
 * MANUAL_ICONS below.
 *
 * Usage:
 *   node scripts/generate-icon-list.mjs
 *   npm run icons     # via package.json
 */

import { readFile, readdir, mkdir, writeFile } from 'node:fs/promises'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const SCAN_DIR = join(ROOT, 'app')
const OUTPUT_FILE = join(ROOT, 'icon/icon.ts')

const SCAN_EXTENSIONS = new Set(['.vue', '.ts', '.tsx', '.js', '.mjs'])
const SKIP_DIRS = new Set(['node_modules', '.nuxt', '.output', 'dist', '.git'])

// Icons referenced indirectly (e.g. through props passed at runtime) that the
// regex below cannot find. Add manually if you spot a missing icon at runtime.
const MANUAL_ICONS = []

// Collection prefixes (e.g. `lucide`, `mdi`) and icon names use lowercase
// letters, digits and hyphens. The regex is intentionally strict to avoid
// false positives like `12:34` time strings or `https:` URL prefixes.
const ICON_PATTERN = /\b([a-z][a-z0-9-]{1,30}):([a-z][a-z0-9-]{1,60})\b/g

// Known Iconify collection prefixes — used as a final allowlist so that
// arbitrary `foo:bar` strings (CSS selectors, etc.) cannot leak into the icon
// bundle. Extend this list when introducing icons from a new collection.
const KNOWN_COLLECTIONS = new Set([
  'ant-design',
  'carbon',
  'cuida',
  'fluent-emoji-flat',
  'icon-park-outline',
  'iconoir',
  'line-md',
  'lucide',
  'material-symbols',
  'mdi',
  'ph',
  'simple-icons',
  'svg-spinners',
  'tabler',
  'uil'
])

async function* walk(dir) {
  let entries
  try {
    entries = await readdir(dir, { withFileTypes: true })
  } catch {
    return
  }
  for (const entry of entries) {
    if (SKIP_DIRS.has(entry.name)) continue
    const full = join(dir, entry.name)
    if (entry.isDirectory()) {
      yield* walk(full)
    } else if (entry.isFile()) {
      const dot = entry.name.lastIndexOf('.')
      if (dot >= 0 && SCAN_EXTENSIONS.has(entry.name.slice(dot))) {
        yield full
      }
    }
  }
}

async function main() {
  const found = new Set(MANUAL_ICONS)
  let scanned = 0

  for await (const file of walk(SCAN_DIR)) {
    scanned++
    const content = await readFile(file, 'utf8')
    let match
    ICON_PATTERN.lastIndex = 0
    while ((match = ICON_PATTERN.exec(content)) !== null) {
      const [, collection, icon] = match
      if (!KNOWN_COLLECTIONS.has(collection)) continue
      found.add(`${collection}:${icon}`)
    }
  }

  const names = [...found].sort()
  const collections = [...new Set(names.map((n) => n.split(':')[0]))].sort()

  const body = render(names, collections)
  await mkdir(dirname(OUTPUT_FILE), { recursive: true })
  await writeFile(OUTPUT_FILE, body)

  const rel = relative(ROOT, OUTPUT_FILE)
  console.log(
    `Scanned ${scanned} files, found ${names.length} icons across ${collections.length} collections`
  )
  console.log(`Wrote ${rel}`)
}

function render(names, collections) {
  const namesLines = names.map((n) => `  '${n}'`).join(',\n')
  const collectionsLines = collections.map((c) => `  '${c}'`).join(',\n')
  return `/**
 * Auto-generated by scripts/generate-icon-list.mjs.
 * Do not edit by hand — run \`npm run icons\` after updating icon usage.
 */
export const ICON_NAMES: string[] = [
${namesLines}
]

export const ICON_COLLECTIONS: string[] = [
${collectionsLines}
]
`
}

main().catch((err) => {
  console.error(err)
  process.exitCode = 1
})
